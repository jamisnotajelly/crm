	DISCOUNTED_PRICE_UAH VARCHAR(255),
	EMAIL VARCHAR(255),
	EXPORT_DATE VARCHAR(255),
	INDEX VARCHAR(255),
	NAME VARCHAR(255),
	ORDER_ID VARCHAR(255),
	PAYER_COMMENT VARCHAR(255),
	PAYMENT_TYPE VARCHAR(255),
	PHONE VARCHAR(255),
	PRICE VARCHAR(255),
	SALES_COMMENT VARCHAR(255),
	SOURCE VARCHAR(255),
	STATE VARCHAR(255),
